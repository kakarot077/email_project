        self.fc2 = nn.Linear(1024, 1024)
        self.gn2 = nn.GroupNorm(16, 1024)
        self.fc3 = nn.Linear(1024, 512)
        self.gn3 = nn.GroupNorm(16, 512)
        self.fc_out = nn.Linear(512, out)
        self.relu = nn.ReLU()
        self.drop = nn.Dropout(0.1)

