class PATE:
    def __init__(self, n_teachers=30, noise=1.0):
        self.n_teachers = n_teachers
        self.noise = noise
        self.teachers = []

    def fit(self, X, y):
        n = len(X)
        idx = np.random.RandomState(42).permutation(n)
        ps = n // self.n_teachers
        for i in range(self.n_teachers):
            s_idx = i * ps
            e_idx = (i + 1) * ps if i < self.n_teachers - 1 else n
            t = ExtraTreesClassifier(
                n_estimators=100, random_state=42 + i, n_jobs=-1
            )
            t.fit(X[idx[s_idx:e_idx]], y[idx[s_idx:e_idx]])
            self.teachers.append(t)
            if (i + 1) % 10 == 0:
                print(f"    Teacher {i+1}/{self.n_teachers}")
        return self

    def predict(self, X):
        votes = np.zeros((len(X), 3))
        for t in self.teachers:
            p = t.predict(X)
            for i in range(len(X)):
                votes[i, p[i]] += 1
        votes += np.random.laplace(0, self.noise, votes.shape)
        return np.argmax(votes, axis=1)

    def approx_eps(self, n_queries):
        return 2.0 * n_queries / (self.noise * self.n_teachers)


