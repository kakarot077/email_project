class DualLayerClassifier:
    def __init__(self, l1, l2, verbose=True):
        self.l1, self.l2, self.verbose = l1, l2, verbose

    def fit(self, X, y):
        y1 = np.where(y == 2, 1, 0)
        if self.verbose:
            print(f"    L1: Phishing={np.sum(y1==1)}, Hybrid={np.sum(y1==0)}")
        self.l1.fit(X, y1)
        mask = (y != 2)
        if self.verbose:
            print(f"    L2: Ham={np.sum(y[mask]==0)}, Spam={np.sum(y[mask]==1)}")
        self.l2.fit(X[mask], y[mask])
        return self

    def predict(self, X):
        final = np.zeros(len(X), dtype=int)
        p1 = self.l1.predict(X)
        final[p1 == 1] = 2
        h = (p1 == 0)
        if np.any(h):
            final[h] = self.l2.predict(X[h])
        return final


