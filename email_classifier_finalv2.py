class EmailANN(nn.Module):
    def __init__(self, dim, out, drop=0.3):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(dim, 1024), nn.BatchNorm1d(1024),
            nn.LeakyReLU(0.1), nn.Dropout(drop),
            nn.Linear(1024, 512), nn.BatchNorm1d(512),
            nn.LeakyReLU(0.1), nn.Dropout(drop),
            nn.Linear(512, 256), nn.BatchNorm1d(256),
            nn.LeakyReLU(0.1), nn.Dropout(drop * 0.7),
            nn.Linear(256, 128), nn.BatchNorm1d(128),
            nn.LeakyReLU(0.1), nn.Dropout(drop * 0.5),
            nn.Linear(128, out))

    def forward(self, x):
        return self.net(x)


def train_ann(X_tr, y_tr, dim, out, epochs=80, bs=64, lr=0.001):
    dev = 'cuda' if torch.cuda.is_available() else 'cpu'
    Xtr, Xv, ytr, yv = train_test_split(
        X_tr, y_tr, test_size=0.15, random_state=42, stratify=y_tr
    )
    sc = StandardScaler()
